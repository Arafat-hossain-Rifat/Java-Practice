public class Largestof3numbers {

	public static void main(String[] args) {
		
		int a=71;
		int b=92;
		int c=55;
		
		if(a>b && a>c)
		{
			System.out.println(a+ "is largest");
		}
		else if(b>a && b>c)
		{
			System.out.println(b+ "is largest");
		}
		else
		{
			System.out.println(c+ "is largest");
		}

	}

}
