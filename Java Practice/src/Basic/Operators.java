package Basic;

public class Operators {

	public static void main(String[] args) {
	       int a=20;
	       int b=10;
	       
	       //Arithmatic Operators
	       
	       System.out.println("Arithmatic Operators"); 
	       System.out.println("sum of a and b:" +(a+b));
	       System.out.println("difference of a and b:" +(a-b));
	       System.out.println("multiplication of a and b:" +(a*b));
	       System.out.println("division of a and b:" +(a/b));
	       System.out.println("modulubation of a and b:" +(a%b));
	       System.out.println("Over Arithmatic Operators"); 
	       
	       //Relational Operators(Return True/False)
	       
	       System.out.println("Relational Operators"); 
	       System.out.println(a==b);
	       System.out.println(a!=b);
	       System.out.println(a<b);
	       System.out.println(a>b);
	       System.out.println(a>=b);
	       System.out.println(a<=b);
	       System.out.println("Over Relational Operators"); 
	       
	       //Logical Operators(Works Between two Booleans)
	       
	       boolean x=true;
	       boolean y=false;
	       System.out.println("Logical Operators");
	       System.out.println(x&&y);
	       System.out.println(x||y);
	       System.out.println(!x);
	       System.out.println(!y);
	       System.out.println("Over Logical Operators");
	       
	       //Increment/Decrement operators
	       
	        a=10;
	       a++;
	       System.out.println(a);
	       
	       b=20;
	       b--;
	       System.out.println(b);
	       
	       

	}

}
